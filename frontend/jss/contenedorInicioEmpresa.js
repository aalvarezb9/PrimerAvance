function mostrarImagenes(){
    document.getElementById('carouselExampleIndicators').style.display = 'block';
    document.getElementById('ocultar-imagenes-empresa').style.display = 'block';
    document.getElementById('ver-imagenes-empresa').style.display = 'none';

}

function ocultarImagenes(){
    document.getElementById('carouselExampleIndicators').style.display = 'none';
    document.getElementById('ocultar-imagenes-empresa').style.display = 'none';
    document.getElementById('ver-imagenes-empresa').style.display = 'block';
}